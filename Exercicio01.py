#Exercicio: Escreva um programa que soma dois números inteiros inseridos pelo usuário.

numero_1=int(input("Olá usuario, insira o primeiro numero inteiro: "))
numero_2=int(input("Olá usuario, insira o segundo numero inteiro: "))
soma_total=numero_1+numero_2
print(f'O valor da soma dos valores digitados é {soma_total}')